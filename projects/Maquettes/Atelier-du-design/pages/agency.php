            <h2>l'agence</h2>
            <p>
Créée en 2010 par RENÉ SENS et LAURE BRILLE, l’atelier du design est aujourd’hui emmenée par ses quatre associés. L’agence d’architecture et de design s’est construite comme une structure ouverte, favorisant le dialogue, la diversité et les coopérations. 

Ces libertés d’échanges et d’associations l’ont vite positionnée au coeur de groupements pluridisciplinaires, affirmant la force et l’efficacité du «travailler ensemble». Architectes & designer, participent à cette dynamique. C’est ainsi, par la diversité d’échelles, de programmes et d’approches que naissent les plus sensibles inventions et les plus riches collaborations.

L’équipe de l’atelier du design a appris à bousculer ses certitudes, à croiser les regards, à convoquer l’interdisciplinarité de l’équipe pour ouvrir le dialogue, à inviter des acteurs extérieurs pour réunir maitrise d’oeuvre et maitrise d’ouvrage autour de réflexions architecturales et urbaines durables : l’architecte ne peut pas faire cavalier seul. Nous cherchons à rendre l’architecture actrice de son territoire (ressources naturelles, connexions urbaines…), à proposer des solutions justes et pérennes, tout en respectant les coûts et la matière et en s’engageant vers la voie du durable. L’objectif de l’agence est de s’éloigner des réflexions standardisées pour innover face aux contraintes quotidiennes que rencontrent les acteurs de la ville.

Les expériences universitaires et professionnelles très différentes de chacun nous permettent de nous construire un solide bagage commun, que nous complétons par les différentes formations auxquelles certains d’entre nous sont inscrits.
Nos compétences informatiques et notre maîtrise des logiciels BIM (Building Information Modeling) nous permettent d’interagir efficacement avec les différents intervenants sur des projets importants.

Cela nous amène à faire profiter les maîtres d’ouvrages que nous assistons des dernières technologies ainsi que des moyens de représentations les plus récents.
